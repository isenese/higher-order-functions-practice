function isLanguageDiverse(list) {

  var python = list.filter(developer => developer.language === 'Python')
  var ruby = list.filter(developer => developer.language === 'Ruby')
  var javascript = list.filter(developer => developer.language === 'JavaScript')
  // thank you for checking out the Coding Meetup kata :)
  var rl = ruby.length;
  var pl = python.length;
  var js = javascript.length;


  if ((pl <= 2*js && pl <= 2*rl) && (rl <= 2*js && rl <= 2*pl) && (js <= 2*rl && js <=2*pl)) {
    return true
  } else {
    return false
  }

}


